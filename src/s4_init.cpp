void s4_mk1_init(UsbDevice& device) {
    cout << "[init] Starting S4 MK1 initialization sequence..." << endl;

    // Phase 1: wake the command channel
    cout << "[init] Phase 1: wake CMD..." << endl;
    cmd(device, {0x01});

    // Phase 2: enumerate the 6 sub-channels + finalize
    cout << "[init] Phase 2: enumerate sub-channels..." << endl;
    cmd(device, {0x01, 0x00});
    cmd(device, {0x14, 0x00});
    cmd(device, {0x14, 0x01});
    cmd(device, {0x14, 0x02});
    cmd(device, {0x14, 0x03});
    cmd(device, {0x14, 0x04});
    cmd(device, {0x14, 0x05});
    cmd(device, {0x0b, 0xff, 0x04, 0x05});

    // Phase 3: first LED wake packet (partial – clears VU meters)
    cout << "[init] Phase 3: LED wake packet..." << endl;
    {
        static const uint8_t wake_pkt[32] = {
            0x0d,0x1f, 0x88,0x1f, 0x89,0x1f, 0x8a,0x1f, 0x8b,0x1f,
            0x8c,0x1f, 0x8d,0x1f, 0x8e,0x1f, 0x8f,0x1f,
            0xff,0xaa, 0xff,0xaa, 0xff,0xaa, 0xff,0xaa, 0xff,0xaa, 0xff,0xaa, 0xff,0xaa
        };
        led_async(device, wake_pkt, 32);
    }

    // Phase 4: more command handshake
    cout << "[init] Phase 4: CMD handshake round 2..." << endl;
    cmd(device, {0x01, 0x00});
    cmd(device, {0x01, 0x78});
    cmd(device, {0x09, 0x01, 0x02, 0x70, 0x00, 0x01});

    // Phase 5: Traktor-ready sequence (triggers full LED init acceptance)
    cout << "[init] Phase 5: Traktor-ready unlock..." << endl;
    cmd(device, {0x01, 0x00});
    cmd(device, {0x09, 0x01, 0x02, 0x70, 0x00, 0x01});
    cmd(device, {0x09, 0xff, 0x00, 0x00, 0x00, 0x01});
    cmd(device, {0x01, 0x00});
    cmd(device, {0x09, 0x01, 0x02, 0x70, 0x00, 0x01});

    // Drain ep 0x84 during LED dump so the device's sensor TX buffer never
    // fills up and stalls endpoint processing (seen as LIBUSB_ERROR_TIMEOUT
    // on ep 0x08 writes after the first two packets).
    start_drain84(device);

    // Phase 6: full LED state – group A (reset/clear)
    cout << "[init] Phase 6: LED dump A (clear)..." << endl;
    for (int i = 0; i < 11; ++i) {
        cout << "[init]   LED_A packet " << i << endl;
        led_async(device, LED_DUMP_A[i], 32);
    }

    // Phase 7: full LED state – group B (initial lit state)
    cout << "[init] Phase 7: LED dump B (initial state)..." << endl;
    for (int i = 0; i < 11; ++i) {
        cout << "[init]   LED_B packet " << i << endl;
        led_async(device, LED_DUMP_B[i], 32);
    }

    stop_drain84();

    cout << "[init] S4 MK1 initialization complete." << endl;
}
